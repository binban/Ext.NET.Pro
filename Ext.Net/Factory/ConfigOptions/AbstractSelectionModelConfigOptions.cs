using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public abstract partial class AbstractSelectionModel
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("selType", new ConfigOption("selType", null, "", this.SelType ));
                list.Add("allowDeselect", new ConfigOption("allowDeselect", null, false, this.AllowDeselect ));
                list.Add("pruneRemoved", new ConfigOption("pruneRemoved", null, true, this.PruneRemoved ));
                list.Add("mode", new ConfigOption("mode", new SerializationOptions(JsonMode.ToLower), SelectionMode.Single, this.Mode ));

                return list;
            }
        }
    }
}