namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class CapsLockDetector
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("preventCapsLockChar", new ConfigOption("preventCapsLockChar", null, false, this.PreventCapsLockChar ));
                list.Add("capsLockIndicatorIconClsProxy", new ConfigOption("capsLockIndicatorIconClsProxy", new SerializationOptions("capsLockIndicatorIconCls"), "", this.CapsLockIndicatorIconClsProxy ));
