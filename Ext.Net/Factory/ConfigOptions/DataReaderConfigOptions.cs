using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    public abstract partial class DataReader
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("fields", new ConfigOption("fields", new SerializationOptions(JsonMode.AlwaysArray), null, this.Fields ));
                list.Add("emptyFields", new ConfigOption("emptyFields", new SerializationOptions("fields", JsonMode.Raw), "", this.EmptyFields ));
                list.Add("messageProperty", new ConfigOption("messageProperty", null, "", this.MessageProperty ));

                return list;
            }
        }
    }
}