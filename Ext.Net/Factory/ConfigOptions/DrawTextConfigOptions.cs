using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class DrawText
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("autoSize", new ConfigOption("autoSize", null, true, this.AutoSize ));
                list.Add("degrees", new ConfigOption("degrees", null, 0, this.Degrees ));
                list.Add("styleSelector", new ConfigOption("styleSelector", null, "", this.StyleSelector ));
                list.Add("text", new ConfigOption("text", null, "", this.Text ));
                list.Add("textStyle", new ConfigOption("textStyle", new SerializationOptions(JsonMode.Object), null, this.TextStyle ));

                return list;
            }
        }
    }
}