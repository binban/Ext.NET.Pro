using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class GroupTabPanel
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("deferredRender", new ConfigOption("deferredRender", null, false, this.DeferredRender ));
                list.Add("activeGroupProxy", new ConfigOption("activeGroupProxy", new SerializationOptions("activeGroup", JsonMode.Raw), "", this.ActiveGroupProxy ));
                list.Add("activeTabProxy", new ConfigOption("activeTabProxy", new SerializationOptions("activeTab", JsonMode.Raw), "", this.ActiveTabProxy ));
                list.Add("tabWidth", new ConfigOption("tabWidth", null, Unit.Pixel(120), this.TabWidth ));
                list.Add("listeners", new ConfigOption("listeners", new SerializationOptions("listeners", JsonMode.Object), null, this.Listeners ));
                list.Add("directEvents", new ConfigOption("directEvents", new SerializationOptions("directEvents", JsonMode.Object), null, this.DirectEvents ));

                return list;
            }
        }
    }
}