using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    public partial class MenuSeparator
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("hideOnClick", new ConfigOption("hideOnClick", null, false, this.HideOnClick ));
                list.Add("disabled", new ConfigOption("disabled", null, true, this.Disabled ));
                list.Add("itemCls", new ConfigOption("itemCls", null, "", this.ItemCls ));

                return list;
            }
        }
    }
}