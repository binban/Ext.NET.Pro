using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class MonthPicker
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("cancelText", new ConfigOption("cancelText", null, "", this.CancelText ));
                list.Add("okText", new ConfigOption("okText", null, "", this.OkText ));
                list.Add("selectedCls", new ConfigOption("selectedCls", null, "", this.SelectedCls ));
                list.Add("showButtons", new ConfigOption("showButtons", null, true, this.ShowButtons ));
                list.Add("selectedDateProxy", new ConfigOption("selectedDateProxy", new SerializationOptions("value", JsonMode.Raw), null, this.SelectedDateProxy ));
                list.Add("listeners", new ConfigOption("listeners", new SerializationOptions("listeners", JsonMode.Object), null, this.Listeners ));
                list.Add("directEvents", new ConfigOption("directEvents", new SerializationOptions("directEvents", JsonMode.Object), null, this.DirectEvents ));

                return list;
            }
        }
    }
}