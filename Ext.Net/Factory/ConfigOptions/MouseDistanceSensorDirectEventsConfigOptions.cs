using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class MouseDistanceSensorDirectEvents
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("far", new ConfigOption("far", new SerializationOptions("far", typeof(DirectEventJsonConverter)), null, this.Far ));
                list.Add("near", new ConfigOption("near", new SerializationOptions("near", typeof(DirectEventJsonConverter)), null, this.Near ));
                list.Add("distance", new ConfigOption("distance", new SerializationOptions("distance", typeof(DirectEventJsonConverter)), null, this.Distance ));

                return list;
            }
        }
    }
}