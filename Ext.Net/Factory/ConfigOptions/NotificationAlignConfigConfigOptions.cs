using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class NotificationAlignConfig
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("elProxy", new ConfigOption("elProxy", new SerializationOptions("el", JsonMode.Raw), "", this.ElProxy ));
                list.Add("anchorsProxy", new ConfigOption("anchorsProxy", new SerializationOptions("position"), "", this.AnchorsProxy ));
                list.Add("offsetProxy", new ConfigOption("offsetProxy", new SerializationOptions("offset", JsonMode.Raw), "", this.OffsetProxy ));

                return list;
            }
        }
    }
}