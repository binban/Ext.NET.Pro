using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public abstract partial class PickerField
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("matchFieldWidth", new ConfigOption("matchFieldWidth", null, true, this.MatchFieldWidth ));
                list.Add("openCls", new ConfigOption("openCls", null, "", this.OpenCls ));
                list.Add("pickerAlign", new ConfigOption("pickerAlign", null, "tl-bl?", this.PickerAlign ));
                list.Add("pickerOffset", new ConfigOption("pickerOffset", new SerializationOptions(typeof(IntArrayJsonConverter)), null, this.PickerOffset ));

                return list;
            }
        }
    }
}