using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class SpinnerFieldDirectEvents
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("spin", new ConfigOption("spin", new SerializationOptions("spin", typeof(DirectEventJsonConverter)), null, this.Spin ));
                list.Add("spinUp", new ConfigOption("spinUp", new SerializationOptions("spinup", typeof(DirectEventJsonConverter)), null, this.SpinUp ));
                list.Add("spinDown", new ConfigOption("spinDown", new SerializationOptions("spindown", typeof(DirectEventJsonConverter)), null, this.SpinDown ));

                return list;
            }
        }
    }
}