using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    public partial class Store
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("memoryProxy", new ConfigOption("memoryProxy", new SerializationOptions("proxy", JsonMode.Raw), "", this.MemoryProxy ));
                list.Add("beforeLoadParamsProxy", new ConfigOption("beforeLoadParamsProxy", new SerializationOptions("beforeLoadParams", JsonMode.Raw), "", this.BeforeLoadParamsProxy ));
                list.Add("beforeSaveParamsProxy", new ConfigOption("beforeSaveParamsProxy", new SerializationOptions("beforeSaveParams", JsonMode.Raw), "", this.BeforeSaveParamsProxy ));
                list.Add("listeners", new ConfigOption("listeners", new SerializationOptions("listeners", JsonMode.Object), null, this.Listeners ));
                list.Add("directEvents", new ConfigOption("directEvents", new SerializationOptions("directEvents", JsonMode.Object), null, this.DirectEvents ));

                return list;
            }
        }
    }
}