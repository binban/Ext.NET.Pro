using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Drawing;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Xml.Serialization;

using Newtonsoft.Json;

namespace Ext.Net
{
    /// <summary>
    /// 
    /// </summary>
    public partial class WindowDirectEvents
    {
        /// <summary>
        /// 
        /// </summary>
		[Browsable(false)]
        [EditorBrowsable(EditorBrowsableState.Never)]
        [DesignerSerializationVisibility(DesignerSerializationVisibility.Hidden)]
		[XmlIgnore]
        [JsonIgnore]
        public override ConfigOptionsCollection ConfigOptions
        {
            get
            {
                ConfigOptionsCollection list = base.ConfigOptions;
                
                list.Add("maximize", new ConfigOption("maximize", new SerializationOptions("maximize", typeof(DirectEventJsonConverter)), null, this.Maximize ));
                list.Add("minimize", new ConfigOption("minimize", new SerializationOptions("minimize", typeof(DirectEventJsonConverter)), null, this.Minimize ));
                list.Add("restore", new ConfigOption("restore", new SerializationOptions("restore", typeof(DirectEventJsonConverter)), null, this.Restore ));

                return list;
            }
        }
    }
}